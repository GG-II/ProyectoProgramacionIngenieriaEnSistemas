    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        String bin = binario.getText(), stnum="",stascii="", newbin="";
        StringTokenizer st = new StringTokenizer(bin," ");
        while(st.hasMoreTokens()){
                newbin=newbin+st.nextToken();
                int numero=Integer.parseInt(newbin, 2);
                char ascii = (char) numero;
                stnum=stnum+" "+String.valueOf(numero);
                stascii=stascii+ascii;
                numero=0;
                ascii=' ';
                newbin="";
        }
       
        ASCII.setText(stascii);
        Num.setText(String.valueOf(stnum));
    }//GEN-LAST:event_calcularActionPerformed

